    private void bSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSubmitActionPerformed
        try{
            String carName = tfName.getText();
            String carType = cbCarType.getSelectedItem().toString();
            double carRate = Double.parseDouble(tfRate.getText());
            //create car object and method
            Car car = new Car(carName,carType,carRate);
            car.addCar();
            JOptionPane.showMessageDialog(rootPane, "Successfully added car.");
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Please enter valid car details.");
        }
        catch (IllegalArgumentException ex){ //for enum
            JOptionPane.showMessageDialog(rootPane, "Please enter valid car type.");
        }
    }//GEN-LAST:event_bSubmitActionPerformed

